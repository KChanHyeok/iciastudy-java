package dto;

public class DtoClass {
    private int number;
    private String str;
    private float fnum;

    public void setNumber(int number) {
        this.number = number;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public void setFnum(float fnum) {
        this.fnum = fnum;
    }

    public int getNumber() {
        return number;
    }

    public String getStr() {
        return str;
    }

    public float getFnum() {
        return fnum;
    }
}
